public class concat2 {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        System.out.println(a + b);
        System.out.println("3" + "4");
        System.out.println("" + a + b);
        System.out.println(3 + 4 + a + "" + b + a);
        System.out.println("Result:" + a + b);
        System.out.println("Result:" + (a + b));
    }
}
